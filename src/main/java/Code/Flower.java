package Code;

/**
 *
 * @author Saleem Malik
 */
public class Flower {

    private String name;
    private String meaning;

    public Flower(String name, String meaning) {
        this.name = name;
        this.meaning = meaning;

    }

    public String getName() {
        return name;
    }

    public String getMeaning() {
        return meaning;
    }

}
